public class Ex11 {
    public static void main(String[] args){
        System.out.println( " \\n  => \n 엔터키 " );
        System.out.println( " \\t  => \t 탭키 " );
        System.out.println( " \\\\ => \\ " );
        System.out.println("하늘은 \"파랗\"습니다");
        System.out.println( " \\\' => \' " );
        System.out.println( " \\\" => \" " );
        //이스케이프 문자는 \역슬레쉬를 특정기능문자 앞에 붙혀 그것이 출력되게해줌
    }
}
